  ) tablespace pg_default;

create table
  public.potion_catalog (
    sku text not null,
    red_ml integer not null default 0,
    green_ml integer not null default 0,
    blue_ml integer not null default 0,
    cost integer not null default 0,
    potion_id integer generated by default as identity,
    dark_ml integer not null default 0,
    constraint potion_catalog_pkey primary key (potion_id)
  ) tablespace pg_default;

create table
  public.potion_ledger_entries (
    id bigint generated by default as identity,
    created_at timestamp with time zone not null default now(),
    quantity_delta integer not null,
    potion_id integer not null,
    constraint potion_ledger_enteries_pkey primary key (id),
    constraint potion_ledger_entries_potion_id_fkey foreign key (potion_id) references potion_catalog (potion_id)
  ) tablespace pg_default;